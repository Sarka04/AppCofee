    public void validarCampos(){
       
        Usuario usuario = usuarioDAO.getPreguntaSeguridad(emailUsuario);
        String contraseniaUsuario = usuario.getContrasenia();
        String contra = jTextFieldContrasenia.getText();
        String pregunta = jTextFieldPregunta.getText();
        String respuesta = jTextFieldNvaRespuesta.getText();
        if(contra.equals(contraseniaUsuario) && !pregunta.equals("") && !respuesta.equals("")){
            jButtonGuardar.setEnabled(true);
        } else
            jButtonGuardar.setEnabled(false);
    }
    
